// JavaScript Arithmetic Operators
/*
Operator	Description
+	Addition
-	Subtraction
*	Multiplication
**	Exponentiation (ES2016)
/	Division
%	Modulus (Remainder)
++	Increment
--	Decrement
*/
// Modulus
console.log(10%2)
console.log(10%3)

// Increment
a = 5
console.log(a++) // Post Incremental
console.log(a)
console.log(++a) // Pre Incremental
// Decremental
b = 5
console.log(b--) // Post Incremental
console.log(b)
console.log(--b) // Pre Incremental

